                <h4>About Andrew</h4>
                <p>Andrew Corbett is a young entrepreneur based out of London, Ontario, Canada and is a partner in numerous Hospitality Properties in South Western Ontario. If you are visiting this page, you may have seen Andrew speaking at the Las Vegas Nightclub & Bar Show, at various public speaking arrangements, or you may have met him throughout his travels. Andrew works on his businesses on a full time basis, but has also branched out to offer consulting services for Restaurants and Bars throughout North America.</p>
                <p>“After years of learning how to create, grow and maintain my own hospitality projects, it has helped me learn so much about this business, and I want to pass that knowledge on to other entrepreneurs that need the support,” states Corbett. “I use to find myself staring at my computer screen, wishing there was a resource that could help me with the specific needs that I am looking for in this business, whether it be in marketing and advertising support, operations and staffing questions, brand creation or financial concerns. Finally I am able to help those that need the support like I did.”</p>
                <p>Starting in 2003, Andrew has had his own businesses, beginning with his first Disc Jockey Company at only 14 years old. That small business grew into a large Entertainment Company that provided DJ and Entertainment Services for over 300 events a year in Windsor Ontario. “Being an event promoter for myself, is where I really learned the most about the backbone of the Restaurant and Bar business… how to bring people through the doors,” Corbett claims. “What I really love about this business, is finding ways to put hundreds of people into a room together and driving bills into the tills.”</p>
                <p>At the age of 22, Andrew and his partners opened The Bull & Barrel, his first restaurant and bar based out of Windsor, Ontario Canada. After The Bull & Barrel came The City Grill (a trendy dining hotspot), Level 3 Vodka Emporium (a 21+ Nightclub for young professionals), The Pubclub Whiskey House (a party hotspot for local students) and then they expanded The Bull & Barrel brand to their second location in London Ontario. In late 2016, Andrew and his partners opened their sixth location, The Soup Market, located in the popular Windsor Star building.</p>
